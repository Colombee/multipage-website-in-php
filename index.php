          <p>Depuis janvier 1998, l'asbl FOR.E.T.(FORmation Emploi Tremplin) est reconnue comme Atelier de Formation par le Travail par la COCOF, le FSE, l'IBFFP et l'ORBEM.<p>
          <p>Elle a pour objet de créer et de gérer des actions visant à former et à qualifier prioritairement des jeunes forestois sans emploi et peu scolarisés.</p>
          <p>Notre asbl est membre de la FEBISP (Fédération Bruxelloise des organismes d'Insertion Socio-professionnelle).</p>
          <p>Poursuivant une mission à finalité sociale, l’association développe et coordonne des activités de formation professionnelle et d’économie sociale dans le secteur de la restauration et de l’hôtellerie, en faveur d’adultes sans emploi, peu qualifiés et éloignés du marché du travail et ce dans le respect et la mise en avant de la diversité propre à notre société.</p>
          <p>Soucieux également de notre environnement, nous avons décidé d’utiliser des produits issus de l’agriculture locale e/ou raisonnée pour notre projet d’épicerie durable/traiteur/table d’hôtes qui axe sa production avec une plus grande place pour le « bien manger », de façon saine, respectueuse des cycles naturels des produits et des saisons.</p>
